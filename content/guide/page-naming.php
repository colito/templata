<p><h2>Naming a Page</h2></p>
<br>
<p>
    To name a page, first open square brackets preferrably at the top of the script, and within those square brackets type:
    <b>page:Page Name</b>.
</p>

<p>
    Doing this will add the page name on the tab bar of whichever browser your site is being viewed on.
</p>